# operator perbandingan adalah operator yang bertugas untuk membandingkan antar dua Operand. 
# Jika hasil perbandingan benar, maka akan menghasilkan nilai True, dan sebaliknya jika salah maka akan menghasilkan nilai False.
# jenis-jenis operator perbandingan

#-------------------------------------------
# Simbol         Deskripsi
#-------------------------------------------
#   >            Lebih besar
#   <            Lebih kecil
#  ==            Sama dengan
#  !=            Tidak sama dengan
#  >=            Lebih besar sama dengan
#  <=            Lebih kecil sama dengan

x = 5
y = 2
print(x > y)    # True
print(x < y)    # False
print(x == y)   # False
print(x != y)   # True
print(x >= y)   # True
print(x <= y)   # False
