# operator aritmatika untuk kasus yang berhubungan dengan hitung menghitung.
# jenis-jenis operator aritmatika 
#----------------------------------------------------------
# Simbol operator           Deskripsi
#----------------------------------------------------------
# Penjumlahan (+)           Menjumlahkan 2 buah Operand.
# Pengurangan (-)           Mengurangkan 2 buah Operand.
# Perkalian   (*)           Mengalikan 2 buah Operand.
# Pembagian   (/)           Membagi 2 buah Operand.
# Modulus     (%)           Menghasilkan sisa bagi dari pembagian 2 bilangan.
# Pemangkatan(**)           Memangkatkan nilai Operand.
# Pembagian Bulat (//)      Sama dengan Pembagian hanya saja, angka dibelakang koma akan dihilangkan/dibulatkan.

x = 5
y = 2
print(x + y)    # 7
print(x - y)    # 3
print(x * y)    # 10
print(x / y)    # 2.5
print(x % y)    # 1
print(x ** y)   # 25
print(x // y)   # 2
