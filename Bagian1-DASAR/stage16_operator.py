# Operator python
# Operator digunakan untuk melakukan operasi pada variabel dan nilai.

# menggunakan operator + untuk menjumlahkan dua nilai
print(2 + 3)
print(2 + int("5"))

# menggunakan operator in untuk mencari nilai tertentu
x = ['alice', 'carl', 'eliot', 'gerald']
if 'eliot' in x:
    print("passed")
else:
    print("failed")

# menggunakan operator pencocokan
print(2 == 2)
print(2 != 2)

# Catatan: operator & | hanya untuk menangani biner/bytes.

